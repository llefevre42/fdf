#include "fdf.h"

void	full_tab(t_tri *lst, int ***tab)
{
	int k;
	int i;

	k = 0;
	i = 0;
	i = lst->hauttab * lst->largtab;
	while(k < i)
	{
		tab[0][k][0] = 0;
		tab[0][k][1] = 0;
		tab[0][k][2] = 0;
		k++;
	}
}
